public static class Summa
{
    public static double Coord1(Point3D p1, Point3D p2)
    {
        return ((p1.X + p2.X));
    }
    public static double Coord2(Point3D p1, Point3D p2)
    {
        return ((p1.Y + p2.Y));
    }
    public static double Coord3(Point3D p1, Point3D p2)
    {
        return ((p1.Z + p2.Z));
    }

}
